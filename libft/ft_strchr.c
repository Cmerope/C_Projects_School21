#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char *tmp;

	tmp = (char*)s;
	while (*tmp || c == '\0')
	{
		if (*tmp == (char)c)
			return (tmp);
		tmp++;
	}
	return (0);
}
