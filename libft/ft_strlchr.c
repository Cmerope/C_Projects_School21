#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*tmp;

	tmp = (char*)s;
	while (*tmp >= '\0')
	{
		if (*tmp == c)
			return (tmp);
		tmp++;
	}
	return (0);
}
